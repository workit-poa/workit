#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";

function getArg(flag, defaultValue = "") {
  const index = process.argv.indexOf(flag);
  if (index < 0 || index + 1 >= process.argv.length) return defaultValue;
  return process.argv[index + 1];
}

function uniqueBy(entries, keyFn) {
  const seen = new Set();
  const output = [];
  for (const entry of entries) {
    const key = keyFn(entry);
    if (seen.has(key)) continue;
    seen.add(key);
    output.push(entry);
  }
  return output;
}

function parseLog(text) {
  const aliasPattern = /\|\s*(0\.0\.\d+)\s*-\s*(0x[a-fA-F0-9]{40})\s*-\s*(0x[a-fA-F0-9]{64})\s*-/g;
  const ecdsaPattern = /\|\s*(0\.0\.\d+)\s*-\s*(0x[a-fA-F0-9]{64})\s*-/g;

  const aliasMatches = uniqueBy(
    Array.from(text.matchAll(aliasPattern)).map((match) => ({
      accountId: match[1],
      evmAddress: match[2],
      privateKey: match[3],
    })),
    (row) => `${row.accountId}:${row.privateKey}`,
  );

  if (aliasMatches.length >= 2) {
    return aliasMatches.slice(0, 2);
  }

  const ecdsaMatches = uniqueBy(
    Array.from(text.matchAll(ecdsaPattern)).map((match) => ({
      accountId: match[1],
      privateKey: match[2],
    })),
    (row) => `${row.accountId}:${row.privateKey}`,
  );

  if (ecdsaMatches.length >= 2) {
    return ecdsaMatches.slice(0, 2);
  }

  return [];
}

function buildEnv(records) {
  const [first, second] = records;
  const lines = [
    "# Auto-generated by dev/hedera-local-node/scripts/extract-funded-accounts.mjs",
    "# Generated from Hiero local node logs.",
    "",
    "HEDERA_NETWORK=local",
    "HEDERA_LOCAL_RPC_URL=http://localhost:7546",
    "HEDERA_LOCAL_WS_URL=ws://localhost:8546",
    "HEDERA_LOCAL_CONSENSUS_ENDPOINT=localhost:50211",
    "HEDERA_LOCAL_MIRROR_GRPC_ENDPOINT=localhost:5600",
    "HEDERA_LOCAL_MIRROR_REST_ENDPOINT=http://localhost:5551",
    "",
    `HEDERA_LOCAL_ACCOUNT_ID_1=${first.accountId}`,
    `HEDERA_LOCAL_PRIVATE_KEY_1=${first.privateKey}`,
    `HEDERA_LOCAL_ACCOUNT_ID_2=${second.accountId}`,
    `HEDERA_LOCAL_PRIVATE_KEY_2=${second.privateKey}`,
    "",
    `HEDERA_OPERATOR_ID=${first.accountId}`,
    `HEDERA_OPERATOR_KEY=${first.privateKey}`,
    `OPERATOR_ID=${first.accountId}`,
    `OPERATOR_KEY=${first.privateKey}`,
    "",
    `HEDERA_PRIVATE_KEY=${first.privateKey}`,
    `DEPLOYER_PRIVATE_KEY=${first.privateKey}`,
  ];

  if (first.evmAddress) lines.push(`HEDERA_LOCAL_EVM_ADDRESS_1=${first.evmAddress}`);
  if (second.evmAddress) lines.push(`HEDERA_LOCAL_EVM_ADDRESS_2=${second.evmAddress}`);

  return `${lines.join("\n")}\n`;
}

function main() {
  const logPath = getArg("--log");
  const outPath = getArg("--out");

  if (!logPath || !outPath) {
    console.error("Usage: extract-funded-accounts.mjs --log <log-file> --out <env-file>");
    process.exit(1);
  }

  if (!fs.existsSync(logPath)) {
    console.error(`Log file not found: ${logPath}`);
    process.exit(1);
  }

  const logText = fs.readFileSync(logPath, "utf8");
  const records = parseLog(logText);

  if (records.length < 2) {
    console.error("Could not find two funded account/private-key pairs in logs.");
    process.exit(1);
  }

  const envText = buildEnv(records);
  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, envText, "utf8");

  console.log(`Wrote local funded account env file: ${outPath}`);
  console.log(`Account #1: ${records[0].accountId}`);
  console.log(`Account #2: ${records[1].accountId}`);
}

main();
